package com.ajay;

public class Var {

	public static void main(String[] args) {
		int age = 20;
		System.out.println(age);
		
		float a = 23.456f;
		System.out.println(a);
		
		double price = 231.33333;
		System.out.println(price);
		
		char rank ='1';
		System.out.println(rank);
		
		boolean pass = true;
		System.out.println(pass);
		 
		boolean fail = false;
		System.out.println(fail);

	}

}
